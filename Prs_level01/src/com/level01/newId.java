package com.level01;

public class newId {

	public static void main(String[] args) {
		
		String new_id = "=.=";
				// "z-+.^.";
				// "...!@BaT#*..y.abcdefghijklm";
		
		solution(new_id);
	}
	
	public static String solution(String new_id) {
        String answer = "";
        String temp = "";
	// 1단계 new_id의 모든 대문자를 대응되는 소문자로 치환합니다.
        
       for (int i = 0; i < new_id.length(); i++) {
    	   
    	   if ((65 <= new_id.charAt(i)) && (new_id.charAt(i) <= 90)) {
    		   answer += Character.toLowerCase(new_id.charAt(i));
    	   } else {
    		   answer += new_id.charAt(i); 
    	   }
    	   
       }
       System.out.println("1단계 : " + answer);     
        
	// 2단계 new_id에서 알파벳 소문자, 숫자, 빼기(-), 밑줄(_), 마침표(.)를 제외한 모든 문자를 제거합니다.
       	for (int i = 0; i < answer.length(); i++) {      		
    	   switch (answer.charAt(i)) {
    	   case 'a':
    		   temp += 'a';
    		   break;   
    	   case 'b':
    		   temp += 'b';
    		   break;   
    	   case 'c':
    		   temp += 'c';
    		   break;   
    	   case 'd':
    		   temp += 'd';
    		   break;   
    	   case 'e':
    		   temp += 'e';
    		   break;   
    	   case 'f':
    		   temp += 'f';
    		   break;   
    	   case 'g':
    		   temp += 'g';
    		   break;   
    	   case 'h':
    		   temp += 'h';
    		   break;   
    	   case 'i':
    		   temp += 'i';
    		   break;   
    	   case 'j':
    		   temp += 'j';
    		   break;   
    	   case 'k':
    		   temp += 'k';
    		   break;   
    	   case 'l':
    		   temp += 'l';
    		   break;   
    	   case 'm':
    		   temp += 'm';
    		   break;   
    	   case 'n':
    		   temp += 'n';
    		   break;   
    	   case 'o':
    		   temp += 'o';
    		   break;   
    	   case 'p':
    		   temp += 'p';
    		   break;   
    	   case 'q':
    		   temp += 'q';
    		   break;   
    	   case 'r':
    		   temp += 'r';
    		   break;   
    	   case 's':
    		   temp += 's';
    		   break;   
    	   case 't':
    		   temp += 't';
    		   break;   
    	   case 'u':
    		   temp += 'u';
    		   break;   
    	   case 'v':
    		   temp += 'v';
    		   break;   
    	   case 'w':
    		   temp += 'w';
    		   break;   
    	   case 'x':
    		   temp += 'x';
    		   break;   
    	   case 'y':
    		   temp += 'y';
    		   break;   
    	   case 'z':
    		   temp += 'z';
    		   break;   
    	   case '-':
    		   temp += '-';
    		   break;   
    	   case '_':
    		   temp += '_';
    		   break;   
    	   case '.':
    		   temp += '.';
    		   break;   
    	   case '0':
    		   temp += '0';
    		   break;   
    	   case '1':
    		   temp += '1';
    		   break;   
    	   case '2':
    		   temp += '2';
    		   break;   
    	   case '3':
    		   temp += '3';
    		   break;   
    	   case '4':
    		   temp += '4';
    		   break;   
    	   case '5':
    		   temp += '5';
    		   break;   
    	   case '6':
    		   temp += '6';
    		   break;   
    	   case '7':
    		   temp += '7';
    		   break;   
    	   case '8':
    		   temp += '8';
    		   break;   
    	   case '9':
    		   temp += '9';
    		   break;   
    	   default:
    		   break;
    	   }  	   
       }
       	
       	answer = temp;  
       	temp = "";
       	System.out.println("2단계 : " + answer);   
	// 3단계 new_id에서 마침표(.)가 2번 이상 연속된 부분을 하나의 마침표(.)로 치환합니다.
       	for (int i = 0; i < answer.length(); i++) {
			if (i >= 1 && answer.charAt(i) == '.' && answer.charAt(i-1) == '.') {
				continue;
			} else { 
				temp += answer.charAt(i);
			}
		}
       	answer = temp;
       	temp = "";
       	System.out.println("3단계 : " + answer);
       	
	// 4단계 new_id에서 마침표(.)가 처음이나 끝에 위치한다면 제거합니다.
       	
       	for (int i = 0; i < answer.length(); i++) {
			if (i == 0 && answer.charAt(i) == '.') {
				continue;
			} else if (i == answer.length()-1 && answer.charAt(answer.length()-1) == '.') {
				continue;
			} else {
				temp += answer.charAt(i);
			}

		}       	
       	answer = temp;
       	temp = "";
       	System.out.println("4단계 : " + answer);
	// 5단계 new_id가 빈 문자열이라면, new_id에 "a"를 대입합니다.
       	if (answer == null || answer == "") {
       		
       		answer = "a";
       	}
       	System.out.println("5단계 : " + answer);
	// 6단계 new_id의 길이가 16자 이상이면, new_id의 첫 15개의 문자를 제외한 나머지 문자들을 모두 제거합니다.
       	if (answer.length() > 15) {
       		for (int i = 0; i < 15; i++) {
				temp += answer.charAt(i);
			}
       		answer = temp;
           	temp = "";
           	System.out.println("6단계 : " + answer);      	
       	}
       	
    // 만약 제거 후 마침표(.)가 new_id의 끝에 위치한다면 끝에 위치한 마침표(.) 문자를 제거합니다.

       	if (answer.charAt(answer.length()-1) == '.') {
       		for (int i = 0; i < answer.length()-1; i++) {
    				temp += answer.charAt(i);
    		}
       		answer = temp;
           	temp = "";
           	System.out.println("6단계 : " + answer);   
       	}
       	   	
	// 7단계 new_id의 길이가 2자 이하라면, new_id의 마지막 문자를 new_id의 길이가 3이 될 때까지 반복해서 끝에 붙입니다.
       	if (answer.length() == 2) {
       		temp += answer.charAt(0);
       		temp += answer.charAt(1);
       		temp += answer.charAt(1);
       		
       		answer = temp;
           	temp = "";
           	System.out.println("7단계 : " + answer);   
       		
       	} else if (answer.length() == 1) {
       		temp += answer.charAt(0);
       		temp += answer.charAt(0);
       		temp += answer.charAt(0);  
       		
       		answer = temp;
           	temp = "";
           	System.out.println("7단계 : " + answer);   
       		
       	} 
        
        return answer;
    }
	
	
	
}
